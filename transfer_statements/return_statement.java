package transfer_statements;

public class return_statement {

	public static void main(String[] args) {
		
		//return statement will stop the execution of method
		
		if(true) {
			System.out.println("Hello Java");
			System.out.println("Hello Java");
			return;//stop the method
		}
		if(true) {
			System.out.println("Hello Java");
			System.out.println("Hello Java");
		}
	}

}
